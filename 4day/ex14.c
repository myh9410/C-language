#include <stdio.h>
int main(){
	int n1=20, n2=4;
	
	printf("%d == %d, 결과 : %d\n",n1,n2,n1 == n2);
	printf("%d != %d, 결과 : %d\n",n1,n2,n1 != n2);
	printf("%d >= %d, 결과 : %d\n",n1,n2,n1 >= n2);
	printf("%d <= %d, 결과 : %d\n",n1,n2,n1 <= n2);
	printf("%d > %d, 결과 : %d\n",n1,n2,n1 > n2);
	printf("%d < %d, 결과 : %d\n",n1,n2,n1 < n2);
	
	return 0;
}
